/* Sync wrapper for external class pointer ************************************/

struct ClassPtr(*mut LeanExternalClass);
unsafe impl Send for ClassPtr { }
unsafe impl Sync for ClassPtr { }

/* External class *************************************************************/

static VEC_CLASS_F64: OnceLock<ClassPtr> = OnceLock::new();

unsafe extern "C" fn finalize_f64(ptr: *mut c_void) {
    drop(Box::from_raw(ptr as *mut Arc<Col<f64>>));
}

unsafe extern "C" fn foreach_f64(_ptr: *mut c_void, _child: BLeanObjArg) {
    // No Lean-object children to traverse.
}

fn get_class_f64() -> *mut LeanExternalClass {
    VEC_CLASS_F64
        .get_or_init(|| unsafe {
            ClassPtr(
                lean_register_external_class(
                    Some(finalize_f64),
                    Some(foreach_f64),
                )
            )
        }).0
}

pub unsafe fn wrap_f64(v: Col<f64>) -> LeanObjRes {
    let boxed = Box::into_raw(Box::new(Arc::new(v)));
    faermat_lean_alloc_external(
        get_class_f64(),
        boxed as *mut c_void,
    )
}

pub unsafe fn unwrap_f64(o: BLeanObjArg) -> &'static Col<f64> {
    let ptr = faermat_lean_get_external_data(o);
    &*(ptr as *const Arc<Col<f64>>)
}

/* f64 FFI ********************************************************************/
// f64 (Lean's Float) is unboxed at the ABI level: scalars pass as
// raw C doubles, not as boxed Lean objects. Functions like get, full,
// scale, set, and dot have different C signatures from the boxed-
// scalar versions generated by vector_ffi_boxed! for f32/c64/c32.
// Because of this, f64 FFI functions are written out manually.

/// Create a vector from a flat Array Float. Returns Option (Vector Float):
/// none if data is empty.
#[no_mangle]
pub unsafe extern "C" fn faermat_vec_of_array_f64(data: BLeanObjArg)
    -> LeanObjRes
{
    let vals = array_float_to_vec(data);
    let mut col: Col<f64> = Col::zeros(vals.len());
    col.iter_mut().zip(vals).for_each(|(elem, v)| { *elem = v; });
    mk_option_some(wrap_f64(col))
}

/// Create a zero vector.
#[no_mangle]
pub unsafe extern "C" fn faermat_vec_zeros_f64(n: u64) -> LeanObjRes {
    wrap_f64(Col::zeros(n as usize))
}

/// Create a vector filled with a constant.
#[no_mangle]
pub unsafe extern "C" fn faermat_vec_full_f64(n: u64, val: f64) -> LeanObjRes {
    let col = Col::full(n as usize, val);
    wrap_f64(col)
}

/* Inspection FFI *************************************************************/

#[no_mangle]
pub unsafe extern "C" fn faermat_vec_len_f64(v: BLeanObjArg) -> u64 {
    unwrap_f64(v).nrows() as u64
}

#[no_mangle]
pub unsafe extern "C" fn faermat_vec_get_f64(v: BLeanObjArg, i: u64) -> f64 {
    let col = unwrap_f64(v);
    col[i as usize]
}

/// Extract to a flat Array Float.
#[no_mangle]
pub unsafe extern "C" fn faermat_vec_to_array_f64(v: BLeanObjArg)
    -> LeanObjRes
{
    let col = unwrap_f64(v);
    let n = col.nrows();
    let mut vals = Vec::with_capacity(n);
    for i in 0..n { vals.push(col[i]); }
    vec_to_array_float(&vals)
}

/* Arithmetic FFI *************************************************************/

#[no_mangle]
pub unsafe extern "C" fn faermat_vec_add_f64(a: BLeanObjArg, b: BLeanObjArg)
    -> LeanObjRes
{
    let va = unwrap_f64(a);
    let vb = unwrap_f64(b);
    if va.nrows() != vb.nrows() {
        let msg = c"Vector.add: length mismatch";
        lean_internal_panic(msg.as_ptr());
    }
    let result = va + vb;
    wrap_f64(result)
}

#[no_mangle]
pub unsafe extern "C" fn faermat_vec_sub_f64(a: BLeanObjArg, b: BLeanObjArg)
    -> LeanObjRes
{
    let va = unwrap_f64(a);
    let vb = unwrap_f64(b);
    if va.nrows() != vb.nrows() {
        let msg = c"Vector.sub: length mismatch";
        lean_internal_panic(msg.as_ptr());
    }
    let result = va - vb;
    wrap_f64(result)
}

#[no_mangle]
pub unsafe extern "C" fn faermat_vec_neg_f64(v: BLeanObjArg) -> LeanObjRes {
    let col = unwrap_f64(v);
    let result = -col;
    wrap_f64(result)
}

#[no_mangle]
pub unsafe extern "C" fn faermat_vec_scale_f64(s: f64, v: BLeanObjArg)
    -> LeanObjRes
{
    let col = unwrap_f64(v);
    let result = faer::Scale(s) * col;
    wrap_f64(result)
}

/// Dot product (inner product) of two vectors.
#[no_mangle]
pub unsafe extern "C" fn faermat_vec_dot_f64(a: BLeanObjArg, b: BLeanObjArg)
    -> f64
{
    let va = unwrap_f64(a).as_col_ref();
    let vb = unwrap_f64(b).as_col_ref();
    if va.nrows() != vb.nrows() {
        let msg = c"Vector.dot: length mismatch";
        lean_internal_panic(msg.as_ptr());
    }
    va.transpose() * vb
}

/// L2 norm.
#[no_mangle]
pub unsafe extern "C" fn faermat_vec_norm_f64(v: BLeanObjArg) -> f64 {
    unwrap_f64(v).as_col_ref().norm_l2()
}

/* Mat-vec mul ****************************************************************/

/// Matrix-vector product: M * v -> vector. Panics if dimensions mismatch.
#[no_mangle]
pub unsafe extern "C" fn faermat_mat_vec_mul_f64(m: BLeanObjArg, v: BLeanObjArg)
    -> LeanObjRes
{
    let mat = matrix::unwrap_f64(m).as_ref();
    let col = unwrap_f64(v).as_col_ref();
    if mat.ncols() != col.nrows() {
        let msg = c"Matrix * Vector: dimension mismatch";
        lean_internal_panic(msg.as_ptr());
    }
    let result = mat * col;
    wrap_f64(result)
}

/* Element set ****************************************************************/

#[no_mangle]
pub unsafe extern "C" fn faermat_vec_set_f64(
    v: LeanObjArg, i: u64, val: f64,
) -> LeanObjRes
{
    let ii = i as usize;
    if faermat_lean_is_exclusive(v) {
        let ptr = faermat_lean_get_external_data(v);
        let arc = &mut *(ptr as *mut Arc<Col<f64>>);
        if Arc::strong_count(arc) == 1 {
            Arc::get_mut(arc).unwrap()[ii] = val;
        } else {
            let mut col = (**arc).clone();
            col[ii] = val;
            *arc = Arc::new(col);
        }
        v
    } else {
        let col_ref = unwrap_f64(v);
        let mut col = col_ref.clone();
        col[ii] = val;
        faermat_lean_dec(v);
        wrap_f64(col)
    }
}

