macro_rules! vector_class {
    (
        $T:ty,
        $suffix:ident,
        $Wrapper:ident,
        $LOCK:ident,
        $finalize:ident,
        $foreach:ident,
        $get_class:ident
    ) => {
        struct $Wrapper(*mut LeanExternalClass);
        unsafe impl Send for $Wrapper { }
        unsafe impl Sync for $Wrapper { }
        static $LOCK: OnceLock<$Wrapper> = OnceLock::new();

        unsafe extern "C" fn $finalize(ptr: *mut c_void) {
            drop(Box::from_raw(ptr as *mut Arc<Col<$T>>));
        }
        unsafe extern "C" fn $foreach(
            _ptr: *mut c_void,
            _child: BLeanObjArg,
        ) { }

        fn $get_class() -> *mut LeanExternalClass {
            $LOCK
                .get_or_init(|| unsafe {
                    $Wrapper(
                        lean_register_external_class(
                            Some($finalize),
                            Some($foreach),
                        )
                    )
                }).0
        }

        paste::paste!(
            pub unsafe fn [<wrap_ $suffix>](v: Col<$T>) -> LeanObjRes {
                let boxed =
                    Box::into_raw(Box::new(Arc::new(v)));
                faermat_lean_alloc_external(
                    $get_class(),
                    boxed as *mut c_void,
                )
            }

            pub unsafe fn [<unwrap_ $suffix>](o: BLeanObjArg)
                -> &'static Col<$T>
            {
                let ptr = faermat_lean_get_external_data(o);
                &**(ptr as *const Arc<Col<$T>>)
            }
        );
    }
}

macro_rules! vector_ffi_boxed {
    ($T:ty, $suffix:ident, $mat_suffix:ident) => { paste::paste!(
        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_of_array_ $suffix>](
            data: BLeanObjArg,
        ) -> LeanObjRes
        {
            let vals = <$T as FaerScalar>::array_to_vec(data);
            let mut col: Col<$T> = Col::zeros(vals.len());
            col.iter_mut().zip(vals).for_each(|(elem, v)| { *elem = v; });
            mk_option_some([<wrap_ $suffix>](col))
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_zeros_ $suffix>](n: u64)
            -> LeanObjRes
        {
            [<wrap_ $suffix>](Col::<$T>::zeros(n as usize))
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_full_ $suffix>](
            n: u64,
            val: BLeanObjArg,
        ) -> LeanObjRes
        {
            let v = <$T as FaerScalar>::unbox_scalar(val);
            let col = Col::full(n as usize, v);
            [<wrap_ $suffix>](col)
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_len_ $suffix>](v: BLeanObjArg)
            -> u64
        {
            [<unwrap_ $suffix>](v).nrows() as u64
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_get_ $suffix>](
            v: BLeanObjArg,
            i: u64,
        ) -> LeanObjRes
        {
            let col = [<unwrap_ $suffix>](v);
            <$T as FaerScalar>::box_scalar(col[i as usize])
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_to_array_ $suffix>](
            v: BLeanObjArg,
        ) -> LeanObjRes
        {
            let col = [<unwrap_ $suffix>](v);
            let n = col.nrows();
            let mut vals = Vec::with_capacity(n);
            for i in 0..n { vals.push(col[i]); }
            <$T as FaerScalar>::vec_to_array(&vals)
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_add_ $suffix>](
            a: BLeanObjArg,
            b: BLeanObjArg,
        ) -> LeanObjRes
        {
            let va = [<unwrap_ $suffix>](a);
            let vb = [<unwrap_ $suffix>](b);
            if va.nrows() != vb.nrows() {
                let msg = c"Vector.add: length mismatch";
                lean_internal_panic(msg.as_ptr());
            }
            [<wrap_ $suffix>](va + vb)
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_sub_ $suffix>](
            a: BLeanObjArg,
            b: BLeanObjArg,
        ) -> LeanObjRes
        {
            let va = [<unwrap_ $suffix>](a);
            let vb = [<unwrap_ $suffix>](b);
            if va.nrows() != vb.nrows() {
                let msg = c"Vector.sub: length mismatch";
                lean_internal_panic(msg.as_ptr());
            }
            [<wrap_ $suffix>](va - vb)
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_neg_ $suffix>](v: BLeanObjArg)
            -> LeanObjRes
        {
            let col = [<unwrap_ $suffix>](v);
            [<wrap_ $suffix>](-col)
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_scale_ $suffix>](
            s: BLeanObjArg,
            v: BLeanObjArg,
        ) -> LeanObjRes
        {
            let sv = <$T as FaerScalar>::unbox_scalar(s);
            let col = [<unwrap_ $suffix>](v);
            [<wrap_ $suffix>](faer::Scale(sv) * col)
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_dot_ $suffix>](
            a: BLeanObjArg,
            b: BLeanObjArg,
        ) -> LeanObjRes
        {
            let va = [<unwrap_ $suffix>](a).as_col_ref();
            let vb = [<unwrap_ $suffix>](b).as_col_ref();
            if va.nrows() != vb.nrows() {
                let msg = c"Vector.dot: length mismatch";
                lean_internal_panic(msg.as_ptr());
            }
            <$T as FaerScalar>::box_scalar(va.transpose() * vb)
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_mat_vec_mul_ $suffix>](
            m: BLeanObjArg,
            v: BLeanObjArg,
        ) -> LeanObjRes
        {
            let mat = matrix::[<unwrap_ $mat_suffix>](m).as_ref();
            let col = [<unwrap_ $suffix>](v).as_col_ref();
            if mat.ncols() != col.nrows() {
                let msg = c"Matrix * Vector: dimension mismatch";
                lean_internal_panic(msg.as_ptr());
            }
            [<wrap_ $suffix>](mat * col)
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_vec_set_ $suffix>](
            v: LeanObjArg,
            i: u64,
            val: BLeanObjArg,
        ) -> LeanObjRes
        {
            let ii = i as usize;
            let scalar = <$T as FaerScalar>::unbox_scalar(val);
            if faermat_lean_is_exclusive(v) {
                let ptr = faermat_lean_get_external_data(v);
                let arc =
                    &mut *(ptr as *mut Arc<Col<$T>>);
                if Arc::strong_count(arc) == 1 {
                    Arc::get_mut(arc).unwrap()[ii] = scalar;
                } else {
                    let mut col = (**arc).clone();
                    col[ii] = scalar;
                    *arc = Arc::new(col);
                }
                v
            } else {
                let col_ref = [<unwrap_ $suffix>](v);
                let mut col = col_ref.clone();
                col[ii] = scalar;
                faermat_lean_dec(v);
                [<wrap_ $suffix>](col)
            }
        }

    ); }
}

