/* Conjugate dot product ******************************************************/

#[no_mangle]
pub unsafe extern "C" fn faermat_vec_conj_dot_f64(
    a: BLeanObjArg,
    b: BLeanObjArg,
) -> f64
{
    let va = unwrap_f64(a).as_col_ref();
    let vb = unwrap_f64(b).as_col_ref();
    if va.nrows() != vb.nrows() {
        let msg = c"Vector.conjDot: length mismatch";
        lean_internal_panic(msg.as_ptr());
    }
    va.transpose() * vb
}

#[no_mangle]
pub unsafe extern "C" fn faermat_vec_conj_dot_f32(
    a: BLeanObjArg,
    b: BLeanObjArg,
) -> LeanObjRes
{
    let va = unwrap_f32(a).as_col_ref();
    let vb = unwrap_f32(b).as_col_ref();
    if va.nrows() != vb.nrows() {
        let msg = c"Vector.conjDot: length mismatch";
        lean_internal_panic(msg.as_ptr());
    }
    faermat_lean_box_float32(va.transpose() * vb)
}

#[no_mangle]
pub unsafe extern "C" fn faermat_vec_conj_dot_c64(
    a: BLeanObjArg,
    b: BLeanObjArg,
) -> LeanObjRes
{
    let va = unwrap_c64(a);
    let vb = unwrap_c64(b);
    let n = va.nrows();
    if n != vb.nrows() {
        let msg = c"Vector.conjDot: length mismatch";
        lean_internal_panic(msg.as_ptr());
    }
    let mut sum = num_complex::Complex::new(0.0, 0.0);
    for k in 0..n { sum += va[k].conj() * vb[k]; }
    box_complex_f64(sum)
}

