/* LU *************************************************************************/

macro_rules! lu_ffi {
    ($suffix:ident, $scalar:ty) => { paste::paste!(
        decomp_class!(
            PartialPivLu<$scalar>,
            [<LuPtr $suffix:upper>],
            [<LU_CLS_ $suffix:upper>],
            [<fin_lu_ $suffix>], [<fe_lu_ $suffix>],
            [<cls_lu_ $suffix>],
            [<wrap_lu_ $suffix>],
            [<unwrap_lu_ $suffix>]
        );

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_mat_lu_ $suffix>](m: BLeanObjArg)
            -> LeanObjRes
        {
            let r = <$scalar as DecompScalar>::unwrap_mat(m).as_ref();
            [<wrap_lu_ $suffix>](PartialPivLu::new(r))
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_lu_solve_ $suffix>](
            lu: BLeanObjArg,
            b: BLeanObjArg,
        ) -> LeanObjRes
        {
            let d = [<unwrap_lu_ $suffix>](lu);
            let rhs = <$scalar as DecompScalar>::unwrap_mat(b).as_ref();
            <$scalar as DecompScalar>::wrap_mat(d.solve(rhs))
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_lu_solve_vec_ $suffix>](
            lu: BLeanObjArg,
            b: BLeanObjArg,
        ) -> LeanObjRes
        {
            let d = [<unwrap_lu_ $suffix>](lu);
            let col = <$scalar as DecompScalar>::unwrap_vec(b).as_col_ref();
            let mut rhs: faer::Mat<$scalar> = faer::Mat::zeros(col.nrows(), 1);
            rhs.col_mut(0).copy_from(col);
            let res = d.solve(rhs.as_ref());
            let out = res.col(0).to_owned();
            <$scalar as DecompScalar>::wrap_vec(out)
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_lu_det_ $suffix>](m: BLeanObjArg)
            -> LeanObjRes
        {
            let r = <$scalar as DecompScalar>::unwrap_mat(m).as_ref();
            <$scalar as FaerScalar>::box_scalar(r.determinant())
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_lu_inverse_ $suffix>](
            lu: BLeanObjArg,
        ) -> LeanObjRes
        {
            let d = [<unwrap_lu_ $suffix>](lu);
            <$scalar as DecompScalar>::wrap_mat(d.inverse())
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_lu_L_ $suffix>](lu: BLeanObjArg)
            -> LeanObjRes
        {
            let d = [<unwrap_lu_ $suffix>](lu);
            <$scalar as DecompScalar>::wrap_mat(d.L().to_owned())
        }

        #[no_mangle]
        pub unsafe extern "C" fn [<faermat_lu_U_ $suffix>](lu: BLeanObjArg)
            -> LeanObjRes
        {
            let d = [<unwrap_lu_ $suffix>](lu);
            <$scalar as DecompScalar>::wrap_mat(d.U().to_owned())
        }
    ); }
}

