/* DecompScalar: wrap/unwrap dispatch for macros ******************************/

trait DecompScalar: FaerScalar {
    unsafe fn wrap_mat(m: faer::Mat<Self>) -> LeanObjRes;
    unsafe fn unwrap_mat(o: BLeanObjArg) -> &'static matrix::MatrixInner<Self>;
    unsafe fn wrap_vec(v: faer::Col<Self>) -> LeanObjRes;
    unsafe fn unwrap_vec(o: BLeanObjArg) -> &'static faer::Col<Self>;
}

