use num_complex::Complex;

/// Compute f(A) = V * diag(f(lambda)) * V^* for a real input matrix.
/// Returns a complex result matrix.
unsafe fn matfn_eigen_real_f64<F>(m: BLeanObjArg, f: F)
    -> Option<faer::Mat<Complex<f64>>>
where F: Fn(Complex<f64>) -> Complex<f64>
{
    let r = matrix::unwrap_f64(m).as_ref();
    if r.nrows() != r.ncols() { return None; }
    let evd = Eigen::<f64>::new_from_real(r).ok()?;
    let n = r.nrows();
    let v = evd.U();
    let s = evd.S();
    // Build V * diag(f(lambda))
    let mut vd: faer::Mat<Complex<f64>> = faer::Mat::zeros(n, n);
    for j in 0 .. n {
        let lam = s.column_vector()[j];
        let fl = f(lam);
        for i in 0 .. n {
            vd[(i, j)] = v[(i, j)] * fl;
        }
    }
    let v_adj = v.adjoint();
    Some(&vd * &v_adj)
}

